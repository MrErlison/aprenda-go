// - Crie um programa que utilize a declaração switch, onde o switch statement seja uma variável do tipo string com identificador "esporteFavorito".

package main

import "fmt"

func main() {
	x := 30

	fmt.Print("Valor de x é ")
	switch x {
	case 10:
		fmt.Print(x)
	case 20:
		fmt.Print(x)
	case 30:
		fmt.Print(x)
	}

}
